package dataTypes;

public class AssignmentOperator {
	public static void main (String[] args) {
		/* Assignment operator are (=, +=, -=, /=, and %=)
		 * a = 7 meaning a = 7
		 * a+=7  means a = a +7
		 * a-=7  means a = a -7
		 * a*=7  means a = a *7
		 * a/=7  means a = a /7
		 * a%=7  means a = a %7
		 */
		int a = 87;
	
		System.out.println(a+=7);
		System.out.println(a-=7);
		System.out.println(a*=7);
		System.out.println(a/=7);
		System.out.println(a%=7);
		
	}

}
