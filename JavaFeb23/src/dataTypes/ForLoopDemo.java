package dataTypes;

public class ForLoopDemo {

	public static void main(String[] args) {
		/*For loop needs 3 conditions
		 * for loop syntax
		 * for(initialization / start point, how far/ end point,how will it increase/decrease)
		 * {
		 * code to be executed
		 * }
		 */
		
		// write "I love you" 10 times using for loop
		for (int i=1; i<=10; i++) 
		{
			System.out.println("I Love you");
			
		}
		
		for (int i=1; i<=3; i++)
		{ 
			System.out.println(i + " I am from Brooklyn"); // i + will show number 
		}
		
		/*
		 * Syntax for while loop 
		 * initialization
		 * while (condition){
		 *  //statement
		 *  increment/decrement
		 *  }
		 *  
		 *  syntax for do while loop
		 *  initialization
		 *  do{
		 *  //statement
		 *  } while (condition)
		 *  
		 *  Type "I am from" + variable 3 times using for, while and do while loop
		 *  use string variable for referring to 
		 *  your place
		 * 
		 */
		
	}

}
